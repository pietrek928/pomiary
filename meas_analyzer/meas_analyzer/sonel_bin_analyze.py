from struct import calcsize, unpack_from
from sys import argv


def read_first_bytes(fname, bytes_count):
    with open(fname, 'rb') as f:
        return f.read(bytes_count)


def get_frame_len(fname):
    start_struct = 'HH'
    data = read_first_bytes(fname, calcsize(start_struct))
    start_flags, frame_len = unpack_from(start_struct, data, 0)
    # print(f'Start flags: {start_flags:02X}')
    return frame_len


def lookup_sequence(fname, bin_pattern, offset, cnt):
    frame_len = get_frame_len(fname)
    assert offset + calcsize(bin_pattern) < frame_len

    data = read_first_bytes(fname, frame_len * cnt)
    it = offset
    while it < len(data):
        v, = unpack_from(bin_pattern, data, it)
        yield v
        it += frame_len


def get_diffs(seq):
    seq = tuple(seq)
    return tuple(
        seq[it + 1] - seq[it] for it in range(len(seq) - 1)
    )


def scale_diff_sequences(s1, s2):
    s1 = tuple(s1)
    s2 = tuple(s2)
    avg_s1 = sum(abs(v1) for v1 in s1) / len(s1)
    # avg_s2 = sum(abs(v2) for v2 in s2) / len(s2)
    sc = sum(abs(v1 / v2) for v1, v2 in zip(s1, s2) if v2) / len(s1)
    diff = sum(abs(v1 - v2 * sc) for v1, v2 in zip(s1, s2)) / len(s1)
    return diff / avg_s1, sc


def find_best_fit(fname, bin_pattern, start_off, end_off, seq):
    seq = tuple(seq)
    return min(
        scale_diff_sequences(
            seq, lookup_sequence(fname, bin_pattern, off, len(seq))
        ) + (off,) for off in range(start_off, end_off)
    )


